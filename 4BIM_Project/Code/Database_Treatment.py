def display_images(Liste_img):
    '''This function displays the images that are in the list Liste_img
    Args:
        Liste_img (list): List of images (an image = a list of pixels)
    Returns:
        No return, just display the images that are in Liste_img'''
    n = len(Liste_img)
    dim = int(sqrt(n))+1
    fig = plt.figure()
    i = 0
    for img in Liste_img:
        fig.add_subplot(dim, dim, i+1)
        plt.title(f"Image {i+1}")
        plt.imshow(img)
        i += 1
    plt.show()


