def Recup_list_attributs(fichier):
    '''This function reads the file list_attr_celeba.txt and stores the information that are in in a list
    Args:
        fichier(str) : chemin d'accès du fichier
    Returns:
        List_attributs (list) = list of 1 and -1 for each image, 1st row = name of the attribute, 1st element of each row (except n°0) = name of the image'''
    with open(fichier) as annotations:
        List_attributs = []
        for ligne in annotations:
            L = ligne.split(' ')
            L1 = []
            for element in L:
                if element != '':
                    L1.append(element)
            if len(L1) > 2:
                List_attributs.append(L1)
    return List_attributs



def Recup_attributs(attribut, List_attributs):
    '''This function stores in a dictionary the value of the attribute "attribut" for each image (key = str of the name of the image)
    Args:
        attribut (str):str representing the name of an attribute among  ("5_o_Clock_Shadow", "Arched_Eyebrows", "Attractive", "Bags_Under_Eyes", "Bald", "Bangs", "Big_Lips", "Big_Nose", "Black_Hair", "Blond_Hair", "Blurry", "Brown_Hair", "Bushy_Eyebrows", "Chubby", "Double_Chin", "Eyeglasses", "Goatee", "Gray_Hair", "Heavy_Makeup", "High_Cheekbones", "Male", "Mouth_Slightly_Open", "Mustache", "Narrow_Eyes", "No_Beard", "Oval_Face", "Pale_Skin", "Pointy_Nose", "Receding_Hairline", "Rosy_Cheeks", "Sideburns", "Smiling", "Straight_Hair", "Wavy_Hair", "Wearing_Earrings", "Wearing_Hat", "Wearing_Lipstick", "Wearing_Necklace", "Wearing_Necktie", "Young")
        List_attributs (list): List representing the file with all the attributes, with values 1 if the attribute is present, -1 if it is not
    Returns:
        Dico_attribut (dictionary): dictionary whose keys are the name of the image (ex: "000001.jpg") and the values are -1 or 1 (1 if attribute present, -1 otherwise)'''
    Dico_attribut = {}
    i = 0
    for element in List_attributs[0]:
        if element == attribut:
            break
        else:
            i += 1
    for image in List_attributs[1:-1] :
        num_image = image[0]
        Dico_attribut[num_image] = int(image[i+1])
    return Dico_attribut
